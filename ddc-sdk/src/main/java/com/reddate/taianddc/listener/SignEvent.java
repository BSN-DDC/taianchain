package com.reddate.taianddc.listener;

public class SignEvent {

	private String sender;
	
	private String encodeTransaction;
	
	public String getSender() {
		return sender;
	}

	public void setSender(String sender) {
		this.sender = sender;
	}

	public String getEncodeTransaction() {
		return encodeTransaction;
	}

	public void setEncodeTransaction(String encodeTransaction) {
		this.encodeTransaction = encodeTransaction;
	}
	
}
